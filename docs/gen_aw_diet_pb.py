def add_page_number_footer(doc):
    """全セクションのフッターにページ番号（中央揃え）を追加。
    タイトルページ（第1ページ）は異なるフッターで番号非表示。"""

    def make_page_field(para):
        """段落にPAGEフィールドを追加するヘルパー"""
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER

        def _run_with_field(fchar_type=None, instr=None):
            run = para.add_run()
            run.font.size = Pt(8.5)
            run.font.color.rgb = RGBColor(0x88, 0x88, 0x88)
            if fchar_type:
                fc = OxmlElement('w:fldChar')
                fc.set(qn('w:fldCharType'), fchar_type)
                run._r.append(fc)
            if instr:
                it = OxmlElement('w:instrText')
                it.set('{http://www.w3.org/XML/1998/namespace}space', 'preserve')
                it.text = instr
                run._r.append(it)
            return run

        _run_with_field(fchar_type='begin')
        _run_with_field(instr=' PAGE ')
        _run_with_field(fchar_type='end')

    for sec_idx, section in enumerate(doc.sections):
        # タイトルページ（最初のセクション）は第1ページを異なるフッターに
        section.different_first_page_header_footer = True

        # ── 通常フッター（2ページ目以降） ──────────────────────
        footer = section.footer
        footer.is_linked_to_previous = False

        # 既存の段落をクリア
        for p in footer.paragraphs:
            for run in p.runs:
                run.clear()

        if footer.paragraphs:
            fp = footer.paragraphs[0]
            # 全ランを削除してクリーンな状態に
            for r in list(fp.runs):
                r._r.getparent().remove(r._r)
        else:
            fp = footer.add_paragraph()

        make_page_field(fp)

        # ── 第1ページフッター（タイトルページ）は空白 ──────────
        first_footer = section.first_page_footer
        first_footer.is_linked_to_previous = False
        if first_footer.paragraphs:
            ffp = first_footer.paragraphs[0]
            for r in list(ffp.runs):
                r._r.getparent().remove(r._r)
        else:
            ffp = first_footer.add_paragraph()
        # 空白のまま（ページ番号なし）


def set_page_size(doc):
    """128×182mmのページサイズとマージンを設定"""
    from docx.oxml import OxmlElement
    from docx.oxml.ns import qn

    section = doc.sections[0]
    section.page_width = PG_W
    section.page_height = PG_H
    section.top_margin = MARGIN
    section.bottom_margin = MARGIN
    section.left_margin = MARGIN_INNER
    section.right_margin = MARGIN


def add_run_with_font(para, text, size_pt, bold=False, color=None, font_name="Hiragino Sans"):
    """指定フォントでランを追加"""
    run = para.add_run(text)
    run.bold = bold
    run.font.size = Pt(size_pt)
    if color:
        run.font.color.rgb = color
    # Hiragino Sans / 游ゴシック
    run.font.name = font_name
    rPr = run._r.get_or_add_rPr()
    rFonts = OxmlElement('w:rFonts')
    rFonts.set(qn('w:hint'), 'eastAsia')
    rFonts.set(qn('w:eastAsia'), font_name)
    rPr.insert(0, rFonts)
    return run


def set_para_spacing(para, before_pt=0, after_pt=0, line_rule=None, line_val=None):
    """段落の前後スペースと行間を設定"""
    pPr = para._p.get_or_add_pPr()
    spacing = OxmlElement('w:spacing')
    spacing.set(qn('w:before'), str(int(before_pt * 20)))
    spacing.set(qn('w:after'), str(int(after_pt * 20)))
    if line_rule and line_val:
        spacing.set(qn('w:lineRule'), line_rule)
        spacing.set(qn('w:line'), str(line_val))
    pPr.append(spacing)


def add_page_break(doc):
    """改ページを追加"""
    para = doc.add_paragraph()
    run = para.add_run()
    br = OxmlElement('w:br')
    br.set(qn('w:type'), 'page')
    run._r.append(br)
    para.paragraph_format.space_before = Pt(0)
    para.paragraph_format.space_after = Pt(0)


