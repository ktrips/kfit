def build_toc(doc, methods):
    """目次を生成（正確なページ番号付き）

    ページ構造（改ページ数から計算）:
      p1:  タイトル
      p2:  はじめに
      p3:  Apple WatchとFitingoとは
      p4:  目次（本ページ）
      p5:  Fitingo 20機能（前半: 機能1-7）
      p6:  Fitingo 20機能続き（機能8-15）
      p7:  Fitingo 20機能続き（機能16-20）
      p8:  Daily Routine — 平日（前半）
      p9:  Daily Routine — 平日（中盤）
      p10: Daily Routine — 平日（後半）
      p11: Daily Routine — 週末（前半）
      p12: Daily Routine — 週末（中盤）
      p13: Daily Routine — 週末（後半）
      p14: 曜日別ゴール設定の使い方
      p15: No.001 〜
      p49: No.035（I. エネルギー消費 終）
      p50: No.036（II. 食事管理 開始）
      p70: No.056（III. マインドフルネス 開始）
      p85: No.071（IV. Fitingo連携 開始）
      p114: No.100
      p115: おわりに
      p116: 著者プロフィール
    """
    # ── タイトル ──────────────────────────────────────
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    set_para_spacing(p, before_pt=0, after_pt=16)
    add_run_with_font(p, "目　次", 16, bold=True, color=RGBColor(0x1A, 0x1A, 0x2E))

    def toc_section_row(label, page_num, indent=0, icon=""):
        """目次の1行（セクション見出し）を追加"""
        p = doc.add_paragraph()
        set_para_spacing(p, before_pt=8, after_pt=3)
        if indent > 0:
            pPr = p._p.get_or_add_pPr()
            ind = OxmlElement('w:ind')
            ind.set(qn('w:left'), str(indent))
            pPr.append(ind)
        if icon:
            add_run_with_font(p, icon + "  ", 10)
        add_run_with_font(p, label, 10, bold=True, color=RGBColor(0x1A, 0x73, 0xE8))
        add_run_with_font(p, f"  ……… {page_num}", 9, color=RGBColor(0x88, 0x88, 0x88))

    def toc_method_row(m, page_num):
        """目次の1行（100の方法）を追加"""
        p = doc.add_paragraph()
        set_para_spacing(p, before_pt=0, after_pt=2)
        pPr = p._p.get_or_add_pPr()
        ind = OxmlElement('w:ind')
        ind.set(qn('w:left'), '360')
        pPr.append(ind)
        no_str = f"No.{m['no']:03d}  {m['icon']}  {m['title']}"
        add_run_with_font(p, no_str, 8.5)
        add_run_with_font(p, f"  … {page_num}", 8, color=RGBColor(0xAA, 0xAA, 0xAA))

    # ── 前付け・新セクション ──────────────────────────────
    front_sections = [
        ("はじめに",                               2, "📖"),
        ("Apple WatchとFitingoとは",               3, "⌚"),
        ("Fitingo アプリ 20の機能",                 5, "🌀"),
        ("Daily Routine — 平日の Fitingoルーティン", 8, "📅"),
        ("Daily Routine — 週末の Fitingoルーティン（強化モード）", 11, "🏆"),
        ("曜日別ゴール設定の使い方",                 14, "📊"),
    ]
    for label, pg, icon in front_sections:
        toc_section_row(label, pg, icon=icon)

    # 区切り線
    p = doc.add_paragraph()
    set_para_spacing(p, before_pt=8, after_pt=8)
    add_run_with_font(p, "─" * 28, 7, color=RGBColor(0xCC, 0xCC, 0xCC))

    # ── 100のダイエット方法 ──────────────────────────────
    p = doc.add_paragraph()
    set_para_spacing(p, before_pt=0, after_pt=6)
    add_run_with_font(p, "100のダイエット方法", 11, bold=True, color=RGBColor(0x1A, 0x1A, 0x2E))

    # 各カテゴリの開始ページ（1メソッド=1ページ、No.001=p15）
    NO001_PAGE = 15
    cats = {}
    for m in methods:
        cats.setdefault(m['cat'], []).append(m)

    # カテゴリ別開始メソッドナンバー → ページ番号
    cat_start_pages = {}
    page = NO001_PAGE
    for cat, items in cats.items():
        cat_start_pages[cat] = page
        page += len(items)

    for cat, items in cats.items():
        # カテゴリ見出し（最初のメソッドと同じページ）
        toc_section_row(cat, cat_start_pages[cat])

        for m in items:
            method_page = NO001_PAGE + (m['no'] - 1)
            toc_method_row(m, method_page)

    # ── 後付け ────────────────────────────────────────
    p = doc.add_paragraph()
    set_para_spacing(p, before_pt=8, after_pt=8)
    add_run_with_font(p, "─" * 28, 7, color=RGBColor(0xCC, 0xCC, 0xCC))

    back_sections = [
        ("おわりに",       NO001_PAGE + 100, ""),
        ("著者プロフィール", NO001_PAGE + 101, ""),
    ]
    for label, pg, icon in back_sections:
        p = doc.add_paragraph()
        set_para_spacing(p, before_pt=4, after_pt=2)
        add_run_with_font(p, label, 9.5)
        add_run_with_font(p, f"  ……… {pg}", 9, color=RGBColor(0x88, 0x88, 0x88))


